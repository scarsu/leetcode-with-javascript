/**
 * @param {TreeNode} root
 * @return {number[][]}
 */
var levelOrder = function(root) {
  // 深度优先遍历
  const ret=[]
  dfs(root,0,ret)
  return ret
};

const dfs = (node, depth, arr) => {
  if(!node) return
  arr[depth] = arr[depth] || []
  arr[depth].push(node.val)
  dfs(node.left, depth+1, arr)
  dfs(node.right, depth+1, arr)
}
